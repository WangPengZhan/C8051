//-----------------------------------------------------------------------------
// delay_us_timer1
//-----------------------------------------------------------------------------
//
// Return Value:  ADC positive input
// Parameters:    delay us
//
// Configures     delay us
//
//-----------------------------------------------------------------------------
void delay_us_timer1( uint32_t delay_us )
{
	volatile uint32_t i = 0;
	volatile uint8_t  j = 0;

	for( i = 0; i < delay_us; i++ )
	{
		for( j = 0; j < 8; j++ )
		{
			;
		}
	}
}

